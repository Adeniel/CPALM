						<h1>Présentation du club</h1>
						<h2>Qui sommes nous:</h2>
						<p><img src="image/club1.jpg" alt="Photo des membres" id="club1"></p>
						<p>Le Club Photo de La Madeleine est l'une des nombreuses activités de l'Association Des habitants (A.D.H.) des quartiers La Madeleine - Justices - Saint Léonard, à Angers. Composé d'une cinquantaine de membres, il partage ses locaux entre la rue Gabriel Lecombre (juste derrière l'église St Léonard) et son laboratoire rue du Pré-Pigeon.<br />
						Nul n'est besoin de s'y connaître en photographie pour rejoindre notre association : il vous suffit d'aimer prendre des photos et d'avoir envie d'en savoir un peu plus. <br />
						Nous nous retrouvons tous les vendredi soirs, dans une ambiance très conviviale, pour apprendre et partager notre passion avec d'autres amateurs. Noir et blanc ou couleur; numérique ou argentique; toutes les techniques sont abordées pour porter un autre regard sur le monde qui nous entoure.<br />
						Les thèmes traités sont les techniques de prise de vue, le maniement de l'appareil photo, le développement et de tirage, le portrait, les grands photographes, etc...<br />
						Enfin, chaque année, nous présentons nos réalisations lors d'une exposition au Trois-Mâts, place des Justices à Angers.</p> 

						<h2>Notre fonctionnement:</h2>
						<p>Aucun des membres n'étant photographe de métier, le Club fonctionne avant tout sur un système d'échange et de partage des connaissances.<br />
						Bien évidemment, les plus anciens vont apprendre aux nouveaux membres quelques techniques de base, telles que :<br />
						- la prise en main ou le perfectionnement dans le maniement de leur appareil photo;<br />
						- les règles de base de la photographie en général et de la prise de vue en particulier;<br />
						- les principes de développement et de tirage pour les personnes intéressées par la photo argentique, etc...<br />
						Mais le plus important est avant tout de développer le regard photographique de chacun : pour cela, une seule solution : soumettre ses photos au regard d'autrui. La critique constructive (et toujours bienveillante) des autres membres permet à tous de progresser et de découvrir des approches différentes pour un même sujet.<br />
						Et pour mettre en pratique les conseils prodigués, rien de mieux qu'une petite sortie photo 2 ou 3 fois dans l'année.<br />
						Enfin, si vous hésitez à vous inscrire parce que votre emploi du temps ne vous permet pas d'être présent tous les vendredi, pas de problème : chacun vient quand il peut (les thèmes abordés ne se suivant pas d'une séance à l'autre) ou selon ses envies.</p>

						<h2>Côté pratique:</h2>
						<p>- Les réunions ont lieu tous les vendredi soir à partir de 20h30, hors vacances scolaires.<br />
						- Les séances thématiques se déroulent 64 rue Gabriel Lecombre (portail vert juste derrière l'église St Léonard).<br />
						- Notre laboratoire photographique, situé rue du Pré-Pigeon, est à la disposition des personnes qui souhaitent aborder la photo argentique (développement et tirage). Les nouveaux adhérents pourront s'exercer à cette pratique, accompagnés et conseillés par un ancien membre.<br />
						- La cotisation annuelle est de 20€ (saison 2019-2020) + 7€ d'adhésion à l'A.D.H.</p>
